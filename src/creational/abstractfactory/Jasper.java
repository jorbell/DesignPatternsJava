package creational.abstractfactory;
public class Jasper {

    private Shirt shirt;
    private Hat hat;
    private Pants pants;
    private Shoes shoes;

    /**
     * @return the shirt
     */
    public Shirt getShirt() {
        return shirt;
    }

    /**
     * @param shirt the shirt to set
     */
    public void setShirt(Shirt shirt) {
        this.shirt = shirt;
    }

    /**
     * @return the hat
     */
    public Hat getHat() {
        return hat;
    }

    /**
     * @param hat the hat to set
     */
    public void setHat(Hat hat) {
        this.hat = hat;
    }

    /**
     * @return the pants
     */
    public Pants getPants() {
        return pants;
    }

    /**
     * @param pants the pants to set
     */
    public void setPants(Pants pants) {
        this.pants = pants;
    }

    /**
     * @return the shoes
     */
    public Shoes getShoes() {
        return shoes;
    }

    /**
     * @param shoes the shoes to set
     */
    public void setShoes(Shoes shoes) {
        this.shoes = shoes;
    }

    

}
